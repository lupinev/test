<script>
$('#form_standard').on('submit', (function(e) {

  var pass1 = $('#newpass').val();
  var pass2 = $('#confirmnewpass').val();

  if (pass1 != pass2 && pass1 != '' && pass2 != '') {
    $.pnotify({ title: "Password Mismatch", type: "info", text: "Password entered must be the same" });
  }
  else if($("#form_standard").valid())
  { e.preventDefault();

    $.ajax
    ({	type:"POST", async: false, cache: false, dataType: 'json', contentType: false, processData:false,
      url:"<?php echo site_url('access/changepassword'); ?>",
      data: new FormData(this),
      success: function(data) {
        if (data == "Success") {
          $.pnotify({ title: "Success", type: "success", text: "Password successfully changed. Logging out..." });

           setTimeout(function() { window.location.replace("<?php echo base_url('access');?>") }, 1500);
        } else if (data == "Wrong Old Password") {
          $.pnotify({ title: "Success", type: "info", text: "Wrong Old Password" });
        } else {
          $.pnotify({ title: "We encountered a problem.", type: "error", text: "Please reload the page and try again." });
        }
      },
      error: function(data) {
        $.pnotify({ title: "We encountered a problem!", type: "error", text: "Please reload the page and try again!" });
      }
    })

  } else {
    $.pnotify({ title: "Information", type: "info", text: "Please complete the fields" });
  }
}))
</script>
